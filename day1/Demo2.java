public class Demo2{
	public static void main(String[] args){
		// 화면에 2 + 4 를 더해서 결과값을 표시해라
		System.out.println(2 + 4);
		// 화면에 0.1+0.9를 더해서 결과값을 표시해라
		System.out.println(0.1 + 0.9);
		// 화면에 문자 a를 표시해라
		System.out.println('A');
		// 화면에 안녕하세요 \t(탭)반갑습니다 \n(줄바꿈) 또 만나요를 출력
		System.out.println("안녕하세요\t반갑습니다.\n또 만나요");
		
		System.out.println("책의 제목은 \"이것이 자바다\"입니다.");
		System.out.println("C:\\java_project\\day1");
		System.out.println("true");
		System.out.println(true);
		System.out.println(false);
	}
}